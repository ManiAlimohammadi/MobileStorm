// Deterministic, high-quality product content generated from product name + brand + category.
// All text is in Persian. No AI API needed — rules-based generation from name/category signals.

export interface ProductContent {
  overview: string;
  features: string[];
  specs: { label: string; value: string }[];
  benefits: string[];
  usageScenarios: string[];
  faqs: { q: string; a: string }[];
  seoDescription: string;
}

interface ContentSignals {
  name: string;
  brand: string;
  category: string;
  price: number; // selling price in thousands Toman
}

// --- helpers ---
function containsAny(text: string, ...terms: string[]): boolean {
  const t = text.toLowerCase();
  return terms.some((term) => t.includes(term.toLowerCase()));
}

function extractWattage(name: string): string {
  const m = name.match(/(\d{1,3})\s*W/i);
  return m ? m[1] + "W" : "18W";
}

function extractCableLength(name: string): string {
  const m = name.match(/(\d+[\.\d]*)\s*(متر|m\b)/i);
  return m ? m[1] + " متر" : "1 متر";
}

function priceRange(price: number): "budget" | "mid" | "premium" {
  if (price < 300) return "budget";
  if (price < 1500) return "mid";
  return "premium";
}

