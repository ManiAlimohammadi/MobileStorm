// --- Public API ---

export function generateProductContent(signals: ContentSignals): ProductContent {
  const { name, category } = signals;

  if (category === "شارژرها" || containsAny(name, "شارژر")) return chargerContent(signals);
  if (category === "کابل‌ها" || containsAny(name, "کابل")) return cableContent(signals);
  if (category === "هندزفری و هدفون" || containsAny(name, "هندزفری", "ایرفون", "هدفون", "هدست")) return earphoneContent(signals);
  if (category === "اسپیکر" || containsAny(name, "اسپیکر", "speaker")) return speakerContent(signals);
  if (category === "پاوربانک" || containsAny(name, "پاوربانک")) return powerbankContent(signals);
  if (category === "قاب و کاور" || containsAny(name, "قاب", "کاور")) return caseContent(signals);
  if (category === "هاب و تبدیل" || containsAny(name, "هاب", "تبدیل", "otg")) return hubContent(signals);

  return defaultContent(signals);
}
